package com.grucas.domain.model;

/**
 *
 * @author GrucasDev
 */
public class ClasificacionDeDocumento {
    
    Integer id;
    String nombre;

    public ClasificacionDeDocumento() { 
        id = 0;
        nombre = "";
    }

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    @Override
    public String toString() {
        return nombre;
    }

}